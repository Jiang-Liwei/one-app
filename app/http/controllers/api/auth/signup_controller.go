package auth

import (
	"fmt"
	"forum/app/http/controllers/api"
	"forum/app/models/user"
	"forum/app/requests"
	"github.com/gin-gonic/gin"
	"net/http"
)

type SignUpController struct {
	api.Controller
}

// IsPhoneExist 检测手机是否被注册
func (signup *SignUpController) IsPhoneExist(c *gin.Context) {

	// 初始化请求对象
	request := requests.PhoneExistRequest{}
	fmt.Println(request)
	// 解析 JSON 请求
	if err := c.ShouldBindJSON(&request); err != nil {
		// 解析失败，返回 422 状态码和错误信息
		c.AbortWithStatusJSON(http.StatusUnprocessableEntity, gin.H{
			"error": err.Error(),
		})
		// 打印错误信息
		fmt.Println(err.Error())
		// 出错了，中断请求
		return
	}

	// 表单验证
	errs := requests.ValidatePhoneExist(&request, c)
	// errs 返回长度等于零即通过，大于 0 即有错误发生
	if len(errs) > 0 {
		// 验证失败，返回 422 状态码和错误信息
		c.AbortWithStatusJSON(http.StatusUnprocessableEntity, gin.H{
			"errors": errs,
		})
		return
	}
	//  检查数据库并返回响应
	c.JSON(http.StatusOK, gin.H{
		"exist": user.IsPhoneExist(request.Phone),
	})
}

